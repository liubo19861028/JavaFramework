package com.framework.exception;

/**
 * 对象为空异常
 * @author
 *
 */
public class ObjectNotFoundException extends BaseUncheckedException {
    private static final long serialVersionUID = 115873639138258484L;

    public ObjectNotFoundException(String msgKey, String message, Throwable throwable) {
        super(msgKey, message, throwable);
    }

    public ObjectNotFoundException(String msgKey, String message) {
        super(msgKey, message);
    }

}